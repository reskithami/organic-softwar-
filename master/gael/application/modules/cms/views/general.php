				<p>
Situé en contre-bas de la route de Lausanne au 222, la Plage du Reposoir reste une des seule plage publique de toute la rade de Genève. Propre, calme et silencieuse, elle est depuis longtemps un endroit privilégié et apprécié des Genevois.
Notre centre de Stand Up Paddle est situé sur le site du Reposoir et vous accueil de début mai à fin septembre dans un cadre calme et bénéficiant d'une structure complète (restaurant, buvette, WC, douche, vestiaire, plage de gazon, parking).
                                </p>



<h5><strong>QU'EST CE QUE LE SUP ? (Stand Up Paddle)</strong></h5>

<p>
Le SUP ou Stand Up Paddle est une forme de surf qui nous vient d'Hawaï.
Il se pratique sur une planche longue et large sur laquelle on se tient debout et que l’on propulse à l’aide d’une grande pagaie.
Le SUP est un sport en pleine émergence partout dans le monde sur tous les plans d'eau (vagues, lacs, rivières)</p>
<p>
En Suisse, c’est une activité accessible à tous, attractive, ludique et saine pour la santé. Elle permet de se balader et de découvrir les richesses de la faune et de la flore tout en respectant l’environnement. 
En effet, l’angle de visibilité étant un des grands avantages de cette discipline par rapport à d’autres comme le kayak, il permet par la position droite et haute au dessus de l’eau de voir aussi bien au loin, au dessus de la surface de l’eau, qu’en profondeur.
</p>


</p>    
			</div>
			<div class="col-sm-4 fond">
				<h5><strong>Activité fitness<br></strong></h5>    
				<p>Le mal de dos est un des maux les plus fréquents de notre société. Le stress, les mauvaise postures au travail sont des réels problèmes que la pratique du Stand Up Paddle peut améliorer.
En tant qu'activité fitness, c'est un sport complet qui permet de travailler de façon symétrique votre corps. Les jambes compensent en permanence pour assurer une bonne assiette du flotteur,  le buste (abdos et fessiers) doit être gainé pour ramer et les bras tirent et poussent, le tout de façon parfaitement symétrique, puisqu'il faut ramer de façon alternative à gauche et à droite.
L’Hebdo : “C’est un excellent outil pour travailler les muscles stabilisateurs du tronc, et donc un bon sport pour le dos” dit le docteur Finn Mahler, directeur de l’unité de médecine sportive de l’hopital de la Tour à Meyrin.


Comme toutes activités sportives, il est essentiel que le SUP soit pratiqué correctement et encadré par des professionnels. 
En effet, les mauvaises postures peuvent entraîner des douleurs et le manque d’expérience des accidents. 
Les personnes au bénéfice d’un diplôme de l’IOSUP (International Organization Stand Up Paddle) sont les seules correctement formées pour vous apprendre la technique de rame et pour encadrer des cours en toute sécurité.



Une fois la technique maîtrisée, libre à chacun de naviguer le long de la côte voir même de surfer sur les vagues.

<strong>Il est donc vivement conseillé de bien apprendre à ramer !</strong></p>
